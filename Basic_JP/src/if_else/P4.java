package if_else;
//wajp to check whether given alphabet is upper case or lower case.
public class P4 {
	public static void main(String[] args) {
		char ch='A';
		if (ch>=65&&ch<=90) 
		{
		System.out.println("the Alphabet is Uppercase: "+ch);	
		}
		else
		{
			System.out.println("the Alphabet is Lowercase: "+ch);
		}
	}
}
